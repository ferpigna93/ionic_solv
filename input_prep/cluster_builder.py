def _parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description="Build ion–water clusters with Thomson-optimal O placement.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=textwrap.dedent("""\
            Examples:
              # Na+ (cation, O toward ion)
              python cluster_builder.py -f Na.xyz -c 1 -n 1 2 3 4 --nconformers 5

              # Cl- (anion, H toward ion)
              python cluster_builder.py -f Cl.xyz -c -1 -n 1 2 3 4 --anion

              # Override ion-O distance
              python cluster_builder.py -f Mg.xyz -c 2 -n 6 --ion-o-dist 2.05
        """),
    )
    p.add_argument("-f", "--file", required=True, metavar="XYZ")
    p.add_argument("-c", "--charge", type=int, required=True)
    p.add_argument("-m", "--mult", type=int, default=1)
    p.add_argument("-n", "--n-water", nargs="+", type=int, default=[1, 2, 3, 4], metavar="N")
    p.add_argument("--nconformers", type=int, default=5, metavar="K")
    p.add_argument("--anion", action="store_true",
                   help="Orient water with H toward ion (for anions)")
    p.add_argument("--ion-o-dist", type=float, default=None, metavar="Å")
    p.add_argument("--config", metavar="YAML")
    p.add_argument("--functional", default=None)
    p.add_argument("--basis", default=None)
    p.add_argument("--nproc", type=int, default=None)
    p.add_argument("--mem", default=None)
    p.add_argument("--solvent", default=None)
    p.add_argument("-o", "--outdir", default="cluster_inputs")
    p.add_argument(
        "--view", action="store_true",
        help=(
            "Generate an HTML viewer for each n after building clusters. "
            "In Jupyter: returns nglview widgets. "
            "In CLI: saves <stem>_n<N>_viewer.html (requires rdkit + nglview "
            "for Jupyter; only needs the built-in 3Dmol.js CDN for CLI HTML)."
        ),
    )
    return p.parse_args()


def main() -> None:
    args = _parse_args()

    cfg = CalcConfig.from_yaml(args.config) if args.config else CalcConfig()
    if args.functional: cfg.functional = args.functional
    if args.basis:      cfg.basis_set  = args.basis
    if args.nproc:      cfg.nproc      = args.nproc
    if args.mem:        cfg.mem        = args.mem
    if args.solvent:    cfg.solvent    = args.solvent

    ion_xyz = Path(args.file)
    if not ion_xyz.exists():
        raise FileNotFoundError(ion_xyz)

    outdir = Path(args.outdir)
    print(f"Building clusters: {ion_xyz.name}  n={args.n_water}  conformers={args.nconformers}")
    generate_clusters(
        ion_xyz=ion_xyz,
        charge=args.charge,
        mult=args.mult,
        n_water_list=args.n_water,
        n_conformers=args.nconformers,
        config=cfg,
        outdir=outdir,
        ion_o_dist=args.ion_o_dist,
        o_toward_ion=not args.anion,
        visualize=args.view,
    )
    print(f"\nDone → {outdir}/")


if __name__ == "__main__":
    main()
