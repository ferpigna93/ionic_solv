#!/usr/bin/env python3
"""cluster_builder.py — Build explicit ion–water clusters for the cluster-continuum approach.

The cluster-continuum thermodynamic cycle (Pliego & Riveros 2001, Kelly et al. 2006)
improves absolute solvation free energies for ions by wrapping them with n explicit
water molecules before applying a continuum solvation model:

  Ion(g) + n·H₂O(g) → [Ion·(H₂O)_n](g)          ΔG°_bind   (DFT/gas)
  [Ion·(H₂O)_n](g)  → [Ion·(H₂O)_n](aq)          ΔG°_cont   (DFT/SMD)
  n·H₂O(l)          → n·H₂O(g)                    n·ΔG°_vap  (+6.32 kcal/mol each)

  ΔG°_solv(ion) = ΔG°_bind + ΔG°_cont − n·ΔG°_vap + ΔG°_ss

Oxygen placement strategy — Thomson problem
-------------------------------------------
Water oxygens are placed at equal distance R from the ion and at the maximum
mutual separation solving the Thomson problem: minimise the Coulomb energy

    E = Σ_{i<j} 1 / |r_i − r_j|

Exact solutions are used for n = 1–6; scipy.optimize handles larger n.
Conformers are generated by applying a random global rotation to the whole
optimised cluster, so inter-oxygen distances are always preserved.

Usage:
  python cluster_builder.py -f Na.xyz -c 1 -n 1 2 3 4 --nconformers 5
  python cluster_builder.py -f Cl.xyz -c -1 -n 4 6 --nconformers 5 --anion
  python cluster_builder.py -f Mg.xyz -c 2 -n 6   --ion-o-dist 2.05
"""

